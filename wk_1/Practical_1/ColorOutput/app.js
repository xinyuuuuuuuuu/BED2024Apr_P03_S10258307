console.log(chalk.red('bye bye'));
console.log(chalk.strikethrough.green('hey!'));
console.log(chalk.magenta('meow'));
console.log(chalk.cyan('bruh'));
console.log(chalk.white('xoxo'));
console.log(chalk.gray('TayTay'));
console.log(chalk.bgBlack('tough loveee'));